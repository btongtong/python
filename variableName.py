# 변수 이름 규칙

# 문자 또는 _ 로 시작
name = '1분'
_name = '파이썬'

# 문자, 숫자, _ 로 구성
name123 = 123
_name_456 = 456

# 공백, 특수문자 안됨
# na me = 'na me'
# $_name_^^ = '^^'

# 대소문자 구분
name = 'yumi'
Name = 'Yumi'
NAME = 'YUMI'

# 키워드(예약어) 안됨
# True = 'True'
# False = 'False'

# 소문자 단어, _ 로 구분된 단어들을 권장
name = 'yumi'
my_name = 'Lee yumi'

