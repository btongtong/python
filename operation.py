# 연산자

# 산술 연산자
print(5 + 3)
print(5 - 3)
print(5 * 3)
print(5 / 3)
print(5 % 3)
print(5 // 3)
print(5 ** 3)

# 비교 연산자
print(5 > 3)
print(5 >= 3)
print(5 < 3)
print(5 <= 3)
print(5 == 3)
print(5 != 2)

# 논리 연산자
print(3<5 and 7<5)
print(3<5 or 7<5)
print(not 3<5)

# 포함 연산자
print('c' in 'cat')
print('c' not in 'cat')