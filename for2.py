# for
# for 변수 in 반복 범위 또는 대상:
#   반복 수행 문장
my_list = [1, 2, 3]
for x in my_list:
    print(x)

my_tuple = (1, 2, 3)
for x in my_tuple:
    print(x)

my_dic = {1: '일', 2: '이', 3: '삼'}
for v in my_dic.values():
    print(v) 

for k in my_dic.keys():
    print(k) 

for k, v in my_dic.items():
    print(k, v) 

fruit = 'apple'
for x in fruit:
    print(x)